@dp.message()
async def handle_daily_answer(message: types.Message):
    user_id = message.from_user.id

    # üëá –ï–°–õ–ò —é–∑–µ—Ä –ø–∏—à–µ—Ç –≤ –ø–æ–¥–¥–µ—Ä–∂–∫—É ‚Äî daily –Ω–µ –≤–º–µ—à–∏–≤–∞–µ—Ç—Å—è
    if user_id in SUPPORT_STATE:
        return

    user_state = DAILY_STATE.get(user_id)
    if not user_state:
        return

    if not user_state.get("waiting_answer"):
        return

    # –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º –∫–æ–º–∞–Ω–¥—ã
    if message.text and message.text.startswith("/"):
        return

